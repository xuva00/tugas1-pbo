    private void nopolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nopolActionPerformed
        // TODO add your handling code here:
        
        if(nopol.getSelectedItem()=="Pilih Plat No"){
            jeniskendaraan.setText("");
            hargasewa.setText("");
        }else if(nopol.getSelectedItem()=="E 1578 D"){
            jeniskendaraan.setText("Mobil Pribadi");
            hargasewa.setText("3500000");
        }else if(nopol.getSelectedItem()=="T 4646 U"){
            jeniskendaraan.setText("Mobil Pick Up");
            hargasewa.setText("250000");
        }else if(nopol.getSelectedItem()=="Z 6580 T"){
            jeniskendaraan.setText("Mobil Angkot");
            hargasewa.setText("1000000");
        }else{
            jeniskendaraan.setText("Mobil Truck");
            hargasewa.setText("500000");
        }
    }//GEN-LAST:event_nopolActionPerformed

