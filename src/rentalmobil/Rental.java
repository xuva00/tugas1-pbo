    //method jumlah karakter transaksi
    public void JumlahKarakter(KeyEvent a){
        if(transaksi.getText().length()==4){
            a.consume();
            JOptionPane.showMessageDialog(null,
                    "Maksimal 4 Karakter!",
                    "WARING!!!", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    //method jumlah karakter sewa
    public void JumlahKaraktersewa(KeyEvent c){
        if(lamasewa.getText().length()==2){
            c.consume();
            JOptionPane.showMessageDialog(null,
                    "Maksimal 2 Karakter!",
                    "WARING!!!", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    //method hanya angka
    public void FilterAngka(KeyEvent b){
        if(Character.isAlphabetic(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,
                    "Masukan Hanya Angka",
                    "Peringatan!", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    
